#!/usr/bin/env node
/**
 * Scans Django static image directories and generates require() maps
 * for React Native to bundle card and personality images locally.
 *
 * Usage: node scripts/generate-image-maps.js
 */

const fs = require('fs');
const path = require('path');

const CARD_IMAGE_DIR = path.resolve(__dirname, '../../walletfreak/static/images/credit_cards');
const PERSONALITY_IMAGE_DIR = path.resolve(__dirname, '../../walletfreak/static/images/personalities');
const OUTPUT_DIR = path.resolve(__dirname, '../src/assets');

const IMAGE_EXTENSIONS = ['png', 'jpg', 'jpeg', 'webp', 'avif'];
const REQUIRE_PATH_PREFIX_CARDS = '../../../walletfreak/static/images/credit_cards';
const REQUIRE_PATH_PREFIX_PERSONALITIES = '../../../walletfreak/static/images/personalities';

function getSlugAndExt(filename) {
  const lastDot = filename.lastIndexOf('.');
  if (lastDot === -1) return null;
  const ext = filename.slice(lastDot + 1).toLowerCase();
  const slug = filename.slice(0, lastDot);
  if (!IMAGE_EXTENSIONS.includes(ext)) return null;
  return { slug, ext, filename };
}

function buildImageMap(dir) {
  const files = fs.readdirSync(dir);
  const slugMap = {};

  for (const file of files) {
    const parsed = getSlugAndExt(file);
    if (!parsed) continue;

    const { slug, ext, filename } = parsed;
    // Priority: png > jpg > jpeg > webp > avif
    const priority = IMAGE_EXTENSIONS.indexOf(ext);
    if (!slugMap[slug] || priority < IMAGE_EXTENSIONS.indexOf(slugMap[slug].ext)) {
      slugMap[slug] = { ext, filename };
    }
  }

  return slugMap;
}

function generateMapFile(slugMap, requirePrefix, outputPath, mapName) {
  const entries = Object.entries(slugMap)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([slug, { filename }]) => `  '${slug}': require('${requirePrefix}/${filename}'),`)
    .join('\n');

  const content = `// Auto-generated by scripts/generate-image-maps.js — do not edit manually
/* eslint-disable */
const ${mapName}: Record<string, any> = {
${entries}
};

export default ${mapName};
`;

  fs.writeFileSync(outputPath, content);
  console.log(`Generated ${outputPath} (${Object.keys(slugMap).length} entries)`);
}

// Ensure output dir exists
if (!fs.existsSync(OUTPUT_DIR)) {
  fs.mkdirSync(OUTPUT_DIR, { recursive: true });
}

// Generate card image map
const cardMap = buildImageMap(CARD_IMAGE_DIR);
generateMapFile(
  cardMap,
  REQUIRE_PATH_PREFIX_CARDS,
  path.join(OUTPUT_DIR, 'cardImageMap.ts'),
  'cardImageMap'
);

// Generate personality image map
const personalityMap = buildImageMap(PERSONALITY_IMAGE_DIR);
generateMapFile(
  personalityMap,
  REQUIRE_PATH_PREFIX_PERSONALITIES,
  path.join(OUTPUT_DIR, 'personalityImageMap.ts'),
  'personalityImageMap'
);

console.log('Done!');
